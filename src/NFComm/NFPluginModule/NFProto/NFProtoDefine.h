/**
 * 将字符串data用分隔符sep切分成一个个的token
 *-----------
 * example：
 * char data[] = "123^A33^A^A555";
 * char sep = '^A';
 * char *token = data;
 * foreach_token(token, data, sep)
 * {
 *     printf("token:%s\n", token);
 * }
 */
#ifndef foreach_token
#define foreach_token(var, data, sep)\
         for(char *ptr=data;({\
                var = ptr;\
                while(*ptr && *ptr != sep)\
                {\
                    ptr++;\
                }\
                if(*ptr)\
                {\
                    *ptr++ = '\0';\
                }\
                var != ptr;});)

#endif